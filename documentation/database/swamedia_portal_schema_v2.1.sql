-- ---------------------------------------------------------------------
-- A14. ROLE_PERMISSION (matriks boolean per role × modul)
-- ---------------------------------------------------------------------
-- Desain: satu baris per kombinasi role × modul.
-- Kolom boolean merepresentasikan hak akses langsung — lebih mudah dibaca
-- dan di-maintain dibanding pendekatan atomik many-to-many.
--
-- Catatan kolom:
--   can_create / can_read / can_update / can_delete : hak CRUD standar
--   can_approve  : khusus Pembayaran & Pengeluaran Perusahaan
--                  (Direktur: lihat CATATAN PENTING #8 di bawah — validasi
--                   unit_id = Direktur Utama dilakukan di service layer)
--   can_export   : hak Export Excel & PDF (lintas modul)
--   scope        : 'ALL' = akses seluruh data; 'UNIT_SENDIRI' = hanya
--                   data milik unit karyawan yang login
-- ---------------------------------------------------------------------
CREATE TABLE public.role_permission (
    id          bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    role_id     bigint  NOT NULL REFERENCES public.role(id),
    modul_id    bigint  NOT NULL REFERENCES public.modul(id),
    can_create  boolean NOT NULL DEFAULT false,
    can_read    boolean NOT NULL DEFAULT false,
    can_update  boolean NOT NULL DEFAULT false,
    can_delete  boolean NOT NULL DEFAULT false,
    can_approve boolean NOT NULL DEFAULT false,
    can_export  boolean NOT NULL DEFAULT false,
    scope       varchar(20) NOT NULL DEFAULT 'ALL',
    created_at  timestamp   NOT NULL DEFAULT now(),
    created_by  varchar(100) NOT NULL DEFAULT 'system',
    updated_at  timestamp,
    updated_by  varchar(100),
    CONSTRAINT uq_role_modul UNIQUE (role_id, modul_id),
    CONSTRAINT ck_role_permission_scope CHECK (scope IN ('ALL','UNIT_SENDIRI'))
);

-- ─── Helper macro ───────────────────────────────────────────────────────────
-- Untuk keterbacaan seed data, dipakai shorthand:
--   T = true, F = false
--   urutan kolom: create, read, update, delete, approve, export, scope
-- ────────────────────────────────────────────────────────────────────────────

-- ═══════════════════════════════════════════════════════════════════════════
-- SUPER_ADMIN — full access semua modul
-- ═══════════════════════════════════════════════════════════════════════════
INSERT INTO public.role_permission
    (role_id, modul_id, can_create, can_read, can_update, can_delete, can_approve, can_export, scope)
SELECT r.id, m.id,
    true, true, true, true,
    -- approve hanya untuk modul yang punya fitur approve
    m.kode_modul IN ('PEMBAYARAN','PENGELUARAN_PERUSAHAAN'),
    -- export hanya untuk modul yang relevan laporan
    m.kode_modul IN ('DASHBOARD','PROYEK','REVENUE_UNIT','REVENUE_UNIT_TW',
                     'TARGET_SALES_UNIT','TAGIHAN','PENCAIRAN','PEMBAYARAN',
                     'PENGELUARAN_PERUSAHAAN','CASHFLOW','DAFTAR_SURAT','AUDIT_TRAIL'),
    'ALL'
FROM public.role r, public.modul m
WHERE r.kode_role = 'SUPER_ADMIN';

