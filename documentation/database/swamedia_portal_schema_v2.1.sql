-- F1. Unit + tipe_unit computed
CREATE VIEW public.v_unit AS
SELECT u.*,
    CASE WHEN EXISTS (
        SELECT 1 FROM public.unit c
        WHERE c.parent_unit_id = u.id AND c.is_deleted = false
    ) THEN 'STRUKTURAL' ELSE 'OPERASIONAL' END AS tipe_unit
FROM public.unit u
WHERE u.is_deleted = false;

-- F2. Kontrak payung + status berlaku computed
CREATE VIEW public.v_kontrak_payung AS
SELECT kp.*,
    CASE WHEN kp.tanggal_selesai >= CURRENT_DATE THEN 'BERLAKU'
         ELSE 'KEDALUWARSA' END AS status_berlaku
FROM public.kontrak_payung kp
WHERE kp.is_deleted = false;

-- F3. Realisasi Revenue per unit per triwulan (cash-basis)
CREATE VIEW public.v_realisasi_revenue_tw AS
SELECT
    p.unit_id,
    EXTRACT(YEAR FROM pt.tanggal_pencairan)::int AS tahun,
    CEIL(EXTRACT(MONTH FROM pt.tanggal_pencairan) / 3.0)::int AS triwulan,
    SUM(pt.nilai) AS realisasi
FROM public.pencairan_tagihan pt
JOIN public.tagihan t ON t.id = pt.tagihan_id AND t.is_deleted = false
JOIN public.proyek  p ON p.id = t.proyek_id  AND p.is_deleted = false
WHERE pt.status IN ('PARSIAL','FINAL') AND pt.is_deleted = false
GROUP BY p.unit_id, EXTRACT(YEAR FROM pt.tanggal_pencairan),
         CEIL(EXTRACT(MONTH FROM pt.tanggal_pencairan) / 3.0);

-- F4. Realisasi Sales per unit per triwulan (deal-basis)
CREATE VIEW public.v_realisasi_sales_tw AS
SELECT
    p.unit_id,
    EXTRACT(YEAR FROM p.tanggal_deal)::int AS tahun,
    CEIL(EXTRACT(MONTH FROM p.tanggal_deal) / 3.0)::int AS triwulan,
    SUM(p.nilai_bersih) AS realisasi
FROM public.proyek p
WHERE p.status = 'DEAL_KONTRAK' AND p.tanggal_deal IS NOT NULL AND p.is_deleted = false
GROUP BY p.unit_id, EXTRACT(YEAR FROM p.tanggal_deal),
         CEIL(EXTRACT(MONTH FROM p.tanggal_deal) / 3.0);

-- F5. Posisi Kas (bertitik-tolak dari saldo awal kas terakhir)
CREATE VIEW public.v_posisi_kas AS
WITH saldo AS (
    SELECT nilai, tanggal FROM public.saldo_awal_kas
    ORDER BY tanggal DESC, id DESC LIMIT 1
),
inflow AS (
    SELECT COALESCE(SUM(pt.nilai),0) AS total
    FROM public.pencairan_tagihan pt
    WHERE pt.status IN ('PARSIAL','FINAL') AND pt.is_deleted = false
      AND pt.tanggal_pencairan >= (SELECT tanggal FROM saldo)
),
out_bayar AS (
    SELECT COALESCE(SUM(nilai),0) AS total FROM public.pembayaran
    WHERE status = 'APPROVED' AND tanggal_realisasi IS NOT NULL
      AND tanggal_realisasi >= (SELECT tanggal FROM saldo)
),
out_keluar AS (
    SELECT COALESCE(SUM(nilai),0) AS total FROM public.pengeluaran_perusahaan
    WHERE status = 'APPROVED' AND tanggal_realisasi IS NOT NULL
      AND tanggal_realisasi >= (SELECT tanggal FROM saldo)
)
SELECT
    (SELECT tanggal FROM saldo) AS tanggal_saldo_awal,
    (SELECT nilai FROM saldo)   AS saldo_awal,
    (SELECT total FROM inflow)  AS total_inflow,
    (SELECT total FROM out_bayar) + (SELECT total FROM out_keluar) AS total_outflow,
    (SELECT nilai FROM saldo)
        + (SELECT total FROM inflow)
        - (SELECT total FROM out_bayar)
        - (SELECT total FROM out_keluar) AS posisi_kas;

