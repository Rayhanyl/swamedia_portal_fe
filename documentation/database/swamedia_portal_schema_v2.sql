    updated_at      timestamp,
    updated_by      varchar(100),
    is_deleted      boolean      NOT NULL DEFAULT false,
    CONSTRAINT uq_unit_kode_unit UNIQUE (kode_unit),
    CONSTRAINT uq_unit_nama_unit UNIQUE (nama_unit),
    CONSTRAINT unit_parent_fkey FOREIGN KEY (parent_unit_id) REFERENCES public.unit(id),
    CONSTRAINT ck_unit_status CHECK (status IN ('AKTIF','TIDAK_AKTIF'))
