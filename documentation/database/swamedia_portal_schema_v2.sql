-- ---------------------------------------------------------------------
-- B7. PROYEK
--     nilai_bersih = nilai_proyek - subkon (generated).
--     tanggal_deal diisi otomatis saat status -> DEAL_KONTRAK.
-- ---------------------------------------------------------------------
CREATE TABLE public.proyek (
    id                    bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    kode_proyek           varchar(50)  NOT NULL,
    customer_id           bigint       NOT NULL,
    industri_id           bigint       NOT NULL,
    unit_id               bigint       NOT NULL,
    kontrak_payung_id     bigint,
    kontrak_biasa_id      bigint,
    nama_proyek           varchar(200) NOT NULL,
    departemen            varchar(100),
    nilai_proyek          numeric(18,2) NOT NULL,
    subkon                numeric(18,2) NOT NULL DEFAULT 0,
    nilai_bersih          numeric(18,2) GENERATED ALWAYS AS (nilai_proyek - subkon) STORED,
    pic_sales_id          bigint       NOT NULL,
    pmo_id                bigint,
    no_kontrak            varchar(100),
    tanggal_kontrak       date,
    tanggal_bast          date,
    tanggal_mulai         date,
    tanggal_deal          date,
    target_selesai        date,
    keterangan_pembayaran text,
    status                varchar(50)  NOT NULL DEFAULT 'INFO_PELUANG',
    tahun                 integer      NOT NULL,
    created_at            timestamp    NOT NULL DEFAULT now(),
    created_by            varchar(100) NOT NULL,
    updated_at            timestamp,
    updated_by            varchar(100),
    is_deleted            boolean      NOT NULL DEFAULT false,
    CONSTRAINT uq_proyek_kode UNIQUE (kode_proyek),
    CONSTRAINT proyek_customer_fkey FOREIGN KEY (customer_id) REFERENCES public.customer(id),
    CONSTRAINT proyek_industri_fkey FOREIGN KEY (industri_id) REFERENCES public.industri(id),
    CONSTRAINT proyek_unit_fkey FOREIGN KEY (unit_id) REFERENCES public.unit(id),
    CONSTRAINT proyek_kontrak_payung_fkey FOREIGN KEY (kontrak_payung_id) REFERENCES public.kontrak_payung(id),
    CONSTRAINT proyek_kontrak_biasa_fkey FOREIGN KEY (kontrak_biasa_id) REFERENCES public.kontrak_biasa(id),
    CONSTRAINT proyek_pic_sales_fkey FOREIGN KEY (pic_sales_id) REFERENCES public.karyawan(id),
    CONSTRAINT proyek_pmo_fkey FOREIGN KEY (pmo_id) REFERENCES public.karyawan(id),
    CONSTRAINT ck_proyek_status CHECK (status IN
        ('INFO_PELUANG','UNDANGAN_PENJELASAN','MEETING_INISIASI','PROSES_PROPOSAL',
         'EVALUASI_ADMIN_TEKNIS','DEAL_KONTRAK','GAGAL')),
    CONSTRAINT ck_proyek_tahun CHECK (tahun BETWEEN 2000 AND 2100)
);

